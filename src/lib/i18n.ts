export type TranslationKey = keyof typeof translations.fa;

export function t(lang: Lang, key: TranslationKey): string {
  return (translations[lang] as any)[key] ?? (translations.fa as any)[key] ?? key;
}

export function formatNumber(num: number, lang: Lang): string {
  if (lang === 'en') return num.toLocaleString('en-US');
  if (lang === 'ar') return num.toLocaleString('ar-EG');
  return num.toLocaleString('fa-IR');
}

export function formatPercent(value: number, lang: Lang): string {
  const rounded = Math.round(value);
  if (lang === 'en') return `${rounded}%`;
  if (lang === 'ar') return `${rounded.toLocaleString('ar-EG')}٪`;
  return `${rounded.toLocaleString('fa-IR')}٪`;
}

/** Detect user language from browser timezone/locale */
export function detectLang(): Lang {
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
    if (tz === 'Asia/Tehran' || tz === 'Iran') return 'fa';
    const arabicTZ = [
      'Asia/Riyadh','Asia/Dubai','Asia/Baghdad','Asia/Kuwait','Asia/Qatar',
      'Asia/Bahrain','Asia/Aden','Africa/Cairo','Africa/Tripoli','Africa/Tunis',
      'Africa/Algiers','Africa/Casablanca','Asia/Beirut','Asia/Damascus',
      'Asia/Amman','Asia/Jerusalem',
    ];
    if (arabicTZ.includes(tz)) return 'ar';
    const locale = navigator.language || '';
    if (locale.startsWith('fa')) return 'fa';
    if (locale.startsWith('ar')) return 'ar';
    if (locale.startsWith('en')) return 'en';
  } catch {}
  return 'fa';
}
